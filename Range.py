# Range represents sequence of numbers

r = range(10)  # It's a collection of items from 0 to 9
print(r)
for i in r :
    print(i, end=', ')

print()

r2 = range(5,10)  # It's a collection of items from 5 to 9
for i in r2 : print(i, end =', ')

print()
r3 = range(0,10,2)
for i in r3 : print(i, end =', ')
